void Game::MainLoop()
{
    while (window->isOpen())
    {
        //Request for closing the window
        while(window->pollEvent(*event))
        {
            if(event->type == sf::Event::Closed)
            {
                window->close();
            }
        }
        Input();
        Update();
        Render();  
    }
    Destroy();
}

void Game::Render()
{
    window->clear(sf::Color(31, 37, 49));
    Draw();
    window->display();
}

